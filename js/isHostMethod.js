/*********************************************************************************************
此函数作用为检测某对象中是否有某函数
1. object为要检测的对象
2. property为要检测函数的函数名，函数名请以字符串的形式输入
**********************************************************************************************/


function isHostMethod(object, property) {
	var t = typeof object[property];
	return t == "function" ||
			(!!(t=="object" && object[property])) ||
			t == "unknown";
}